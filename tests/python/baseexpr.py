
print("10 + 1.2 = %s" % (10 + 1.2))
print("10 + 1 = %s" % (10 + 1))
print("10.1 + 1 = %s" % (10.1 + 1))
print("10.1 + 1.2 = %s" % (10.1 + 1.2))

print("10 - 1.2 = %s" % (10 - 1.2))
print("10 - 1 = %s" % (10 - 1))
print("10.1 - 1 = %s" % (10.1 - 1))
print("10.1 - 1.2 = %s" % (10.1 - 1.2))

print("33 * 6 = %s" % (33 * 6))
print("10 / 3 = %s" % (10 / 3))
print("10 %% 3 = %s" % (10 % 3))
